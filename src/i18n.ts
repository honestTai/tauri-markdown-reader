export type Language = keyof typeof uiText
export type UiText = (typeof uiText)[Language]

export function formatArticleCount(count: number, language: Language) {
  return language === 'zh' ? `${count} 篇` : `${count} docs`
}

export function formatWordCount(count: number, language: Language) {
  return language === 'zh' ? `${count} 字` : `${count} words`
}

export function formatReadingMinutes(minutes: number, language: Language) {
  return language === 'zh' ? `${minutes} 分钟` : `${minutes} min`
}

export function formatImageCount(count: number, language: Language, short = false) {
  if (language === 'zh') return short ? `${count} 图` : `${count} 张图片`
  return short ? `${count} img` : `${count} images`
}

export function formatExportSummary(stats: ArticleStats, language: Language) {
  const minutes = stats.readingMinutes || 1
  const images = stats.images || 0
  return language === 'zh' ? `${minutes} 分钟阅读，${images} 张图片` : `${minutes} min read, ${images} images`
}

export function displayGroupName(group: string, language: Language) {
  if (language === 'zh') return group
  const groups: Record<string, string> = {
    示例: 'Demo',
    文档: 'Documents',
    草稿: 'Drafts',
    审稿: 'Review',
    已确认: 'Approved',
    已确认稿: 'Approved',
  }
  return groups[group] || group
}
